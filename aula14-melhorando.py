# if / elif   / else 
# se / se não se / se não

entrada = input("Você gostaria de 'entrar' ou 'sair' ?  ")

if entrada == 'entrar':
    print("Você entrou no sistema !")
elif entrada == 'sair':
    print('Obrigado por usar nossos serviços')
else:
    print("Você não digitou para entrar e nem sair!")